    private void avatarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_avatarMouseClicked
        // Chọn ảnh
        JFileChooser fileChooser = new JFileChooser();
        // Thiết lập chế độ mở tệp tin
        fileChooser.setDialogTitle("Mở tệp tin");
         // Thiết lập bộ lọc cho các tập tin hình ảnh
        fileChooser.setFileFilter(new FileFilter() {
            @Override
            public boolean accept(File f) {
                if (f.isDirectory()) {
                    return true; // Hiển thị thư mục
                }
                // Lấy đuôi tập tin
                String fileName = f.getName().toLowerCase();
                return fileName.endsWith(".jpg") || fileName.endsWith(".jpeg") ||
                       fileName.endsWith(".png") || fileName.endsWith(".gif") ||
                       fileName.endsWith(".bmp");
            }

            @Override
            public String getDescription() {
                return "Image Files (*.jpg, *.jpeg, *.png, *.gif, *.bmp)";
            }
        });
        
        // Hiển thị hộp thoại mở tập tin
        int returnValue = fileChooser.showOpenDialog(QuanLy.this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            try {
                File selectedFile = fileChooser.getSelectedFile();
                Image img = ImageIO.read(selectedFile);
                avatar.setText("");
                int width = avatar.getWidth();
                int height = avatar.getHeight();
                avatar.setIcon(new ImageIcon(img.getScaledInstance(width, height, 0)));
                strHinhAnh = selectedFile.getPath();

            } catch (IOException ex) {
                Logger.getLogger(QuanLy.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_avatarMouseClicked

