    private void btn_saveKeyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveKeyActionPerformed
        // Lưu khóa
        JFileChooser fileChooser = new JFileChooser();
        // Thiết lập chế độ lưu tệp tin
        fileChooser.setDialogTitle("Lưu tệp tin");
        // Thiết lập bộ lọc tệp tin để chỉ hiển thị các tệp tin .txt
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Text files", "txt");
        fileChooser.setFileFilter(filter);
        // Hiển thị hộp thoại lưu tệp tin
        int userSelection = fileChooser.showSaveDialog(QuanLy.this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            if (!fileToSave.getAbsolutePath().endsWith(".txt")) {
                fileToSave = new File(fileToSave + ".txt");
            }
            System.out.println("Lưu tệp tin tới: " + fileToSave.getAbsolutePath());

            // Ghi dữ liệu vào tệp tin
            String data = keyField.getText();
            try (BufferedWriter writer = new BufferedWriter(new FileWriter(fileToSave))) {
                writer.write(data);
                System.out.println("Ghi dữ liệu thành công vào tệp tin.");
            } catch (IOException e) {
                System.out.println("Có lỗi xảy ra khi ghi tệp tin.");
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_saveKeyActionPerformed

